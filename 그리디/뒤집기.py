# 입력받은 string -> 변화 횟수 -> 뒤집어야할 회수
# 0 -> 0 -> 0
# 01 -> 1 -> 1
# 010 -> 2 -> 1
# 0101 -> 3 -> 2
# 01010 -> 4 -> 2
# 010101 -> 5 -> 3
# 0100101 -> 5 -> 3
